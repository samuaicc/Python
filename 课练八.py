#古典问题：有一对兔子，从出生后第三个月起每个月都生一对小兔子，小兔子长到第三个月后每个月又生一对兔子，假如兔子都不死，每个月每个月的兔子总数为
#分析：兔子的生长规律符合数列2*（1,1,2,3,5,8,13,21，，，）
a = int(input("输入要到第几个月："))
a1 = 1
a2 = 1
for i in range(1,a+1):
    if (i <= 2):
        print("第%d个月，总数为2只" % i)
        
    else:
        if i == 3:
            a1 = a2
        else:
            a1,a2 = a2,a1+a2
        print("第%d个月，总数为%d只" % (i,2*(a1+a2)))
